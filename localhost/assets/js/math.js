function sum(a, b){
    return a + b;
}

function substract(a, b){
    return a - b;
}

function multiply(a, b){
    return a * b;
}

function division(a, b){
    if(b == 0){
        return 'Делить на ноль нельзя!';
    }
    else{
        return a / b;
    }
    return a / b;
}



